    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno =jTextField1.getText();
        
        try{
               Class.forName("com.mysql.cj.jdbc.Driver");
               Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","password");
               Statement st = con.createStatement();
               ResultSet rs=st.executeQuery("select * from result where result.rollNumber='"+rollno+"'");
          
               if(jCheckBox1.isSelected())
             {
                 if(rs.next())
                   {              
                    setVisible(false);
                   new studentIndex(rollno).setVisible(true);
                   }
                 else
                   {
                   JOptionPane.showMessageDialog(null,"Incorrect roll number");
                   }
              }
               else
               {
                   JOptionPane.showMessageDialog(null,"Kindly verify first");
               }   
               
            }               
           
           catch(Exception e)
           {
               JOptionPane.showMessageDialog(null,e.toString());
           }
    }//GEN-LAST:event_jButton1ActionPerformed

