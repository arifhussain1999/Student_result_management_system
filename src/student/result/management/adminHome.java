               st.executeUpdate("insert into student(rollno,course,branchName,name,gender,motherName) values('"+roll+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+motherName+"')");
              JOptionPane.showMessageDialog(null,"Data saved successfully");
