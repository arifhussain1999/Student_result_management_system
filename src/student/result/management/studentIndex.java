    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno=jTextField5.getText();
         try{
               Class.forName("com.mysql.cj.jdbc.Driver");
               Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","password");
               Statement st = con.createStatement();
               ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollNumber='"+rollno+"'");

               if(rs.next())
               {
                   jTextField4.setText(rs.getString(4));
                   jTextField3.setText(rs.getString(2));
                   jTextField1.setText(rs.getString(3));
                   jTextField6.setText(rs.getString(6));
                   jTextField2.setText(rs.getString(5));
                   jTextField8.setText(rs.getString(8));
                   jTextField7.setText(rs.getString(9));
                   jTextField10.setText(rs.getString(10));
                   jTextField11.setText(rs.getString(11));
                   jTextField9.setText(rs.getString(12));
                   
                   int s1=Integer.parseInt(jTextField8.getText());
                   int s2=Integer.parseInt(jTextField7.getText());
                   int s3=Integer.parseInt(jTextField10.getText());
                   int s4=Integer.parseInt(jTextField11.getText());
                   int s5=Integer.parseInt(jTextField9.getText());
                   
                   String totalMarks=String.valueOf(s1+s2+s3+s4+s5);
                    jTextField12.setText(totalMarks);
               }
           }
           catch(Exception e)
           {
               JOptionPane.showMessageDialog(null,e.toString());
           }
    }//GEN-LAST:event_formComponentShown

